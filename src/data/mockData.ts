function generateRankHistory(baseRank: number, type: string, peak: number): { time: string; rank: number }[] {
  const hours = ['04:00', '06:00', '08:00', '10:00', '12:00', '14:00']
  const history: { time: string; rank: number }[] = []
  let currentRank = baseRank

  for (let i = 0; i < hours.length; i++) {
    if (type === 'rising') {
      currentRank = Math.max(peak, currentRank - Math.floor(Math.random() * 8) - 2)
    } else if (type === 'falling') {
      currentRank = Math.min(45, currentRank + Math.floor(Math.random() * 6) + 1)
    } else if (type === 'fluctuating') {
      const change = Math.floor(Math.random() * 10) - 5
      currentRank = Math.max(peak, Math.min(45, currentRank + change))
    } else {
      currentRank = Math.max(peak, currentRank - Math.floor(Math.random() * 6))
    }
    history.push({ time: hours[i], rank: currentRank })
  }
  return history
}

